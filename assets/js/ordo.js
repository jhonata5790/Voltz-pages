function renderParticles(elementKey) {
  if (!particleLayer) return;

  const config = particleConfigs[elementKey];
  particleLayer.innerHTML = "";

  if (!config) return;

  for (let i = 0; i < config.count; i++) {
    const particle = document.createElement("span");
    const symbol = config.symbols[Math.floor(Math.random() * config.symbols.length)];

    particle.className = `particle ${config.className}`;
    particle.textContent = symbol;
    particle.style.left = `${Math.random() * 100}%`;
    particle.style.top = `${Math.random() * 100}%`;
    particle.style.fontSize = `${0.7 + Math.random() * 1.4}rem`;
    particle.style.animationDelay = `${Math.random() * 6}s`;
    particle.style.animationDuration = `${config.minDuration + Math.random() * config.variation}s`;

    particleLayer.appendChild(particle);
  }
}

