function startEnergyTextMutation() {
  stopEnergyTextMutation();

  energyMutationTimer = window.setInterval(() => {
    if (currentElement !== "energia") return;

    const targets = document.querySelectorAll("[data-mutation-key]");

    targets.forEach((target) => {
      const key = target.dataset.mutationKey;
      const variants = energyTextVariants[key];

      if (!variants || variants.length === 0) return;

      const nextText = variants[Math.floor(Math.random() * variants.length)];
      scrambleText(target, nextText);
    });
  }, 2600);
}

function stopEnergyTextMutation() {
  if (energyMutationTimer) {
    window.clearInterval(energyMutationTimer);
    energyMutationTimer = null;
  }

  energyScrambleTimers.forEach((timer) => window.clearInterval(timer));
  energyScrambleTimers = [];
}

function scrambleText(element, finalText) {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789@#$%&+-÷×√∴◈✦";
  let frame = 0;
  const maxFrames = 14;
  const textLength = finalText.length;

  const timer = window.setInterval(() => {
    const progress = frame / maxFrames;
    const revealLimit = Math.floor(progress * textLength);

    element.textContent = finalText
      .split("")
      .map((char, index) => {
        if (char === " " || char === "\n") return char;
        if (index < revealLimit) return char;
        return chars[Math.floor(Math.random() * chars.length)];
      })
      .join("");

    frame++;

    if (frame > maxFrames) {
      window.clearInterval(timer);
      element.textContent = finalText;
    }
  }, 34);

  energyScrambleTimers.push(timer);
}


